package kr.ac.kopo.model;

public class EatPlanner {

	private int eatNum;
	private int code;
	private String eatDate;
	private String eatName;
	private float eatGram;
	private int eatCount;
	private float eatKcal;
	private String eatEtc;
	private String eatNutrient;
	private String eatTime;
	private String manager;
	private String username;


	
	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public int getEatNum() {
		return eatNum;
	}

	public void setEatNum(int eatNum) {
		this.eatNum = eatNum;
	}

	public String getEatDate() {
		return eatDate;
	}

	public void setEatDate(String eatDate) {
		this.eatDate = eatDate;
	}

	public String getEatName() {
		return eatName;
	}

	public void setEatName(String eatName) {
		this.eatName = eatName;
	}

	public float getEatGram() {
		return eatGram;
	}

	public void setEatGram(float eatGram) {
		this.eatGram = eatGram;
	}

	public int getEatCount() {
		return eatCount;
	}

	public void setEatCount(int eatCount) {
		this.eatCount = eatCount;
	}

	public float getEatKcal() {
		return eatKcal;
	}

	public void setEatKcal(float eatKcal) {
		this.eatKcal = eatKcal;
	}

	public String getEatEtc() {
		return eatEtc;
	}

	public void setEatEtc(String eatEtc) {
		this.eatEtc = eatEtc;
	}

	public String getEatNutrient() {
		return eatNutrient;
	}

	public void setEatNutrient(String eatNutrient) {
		this.eatNutrient = eatNutrient;
	}

	public String getEatTime() {
		return eatTime;
	}

	public void setEatTime(String eatTime) {
		this.eatTime = eatTime;
	}

	public String getManager() {
		return manager;
	}

	public void setManager(String manager) {
		this.manager = manager;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

}
