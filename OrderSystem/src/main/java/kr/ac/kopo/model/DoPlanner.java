package kr.ac.kopo.model;

public class DoPlanner {

	private int doNum;
	private int code;
	private String doDate;
	private String doName;
	private int doSet;
	private int doReps;
	private int doLb;
	private String doGoal;
	private String doPart;
	private String doEtc;
	private String manager;
	private String username;


	
	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public int getDoNum() {
		return doNum;
	}

	public void setDoNum(int doNum) {
		this.doNum = doNum;
	}

	public String getDoDate() {
		return doDate;
	}

	public void setDoDate(String doDate) {
		this.doDate = doDate;
	}

	public String getDoName() {
		return doName;
	}

	public void setDoName(String doName) {
		this.doName = doName;
	}

	public int getDoSet() {
		return doSet;
	}

	public void setDoSet(int doSet) {
		this.doSet = doSet;
	}

	public int getDoReps() {
		return doReps;
	}

	public void setDoReps(int doReps) {
		this.doReps = doReps;
	}

	public float getDoLb() {
		return doLb;
	}

	public void setDoLb(int doLb) {
		this.doLb = doLb;
	}

	public String getDoGoal() {
		return doGoal;
	}

	public void setDoGoal(String doGoal) {
		this.doGoal = doGoal;
	}

	public String getDoPart() {
		return doPart;
	}

	public void setDoPart(String doPart) {
		this.doPart = doPart;
	}

	public String getDoEtc() {
		return doEtc;
	}

	public void setDoEtc(String doEtc) {
		this.doEtc = doEtc;
	}

	public String getManager() {
		return manager;
	}

	public void setManager(String manager) {
		this.manager = manager;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

}
